package com.mommy.app.ask.vo;

public class AskVO {
	private int askNum;
	private int userNum;
	private int categoryNum;
	private String askTitle;
	private String askContent;
	private String askAttach;
	private String askDate;
	private int askStatus;
	private String askAnswer;
	
	public AskVO() {;}

	public int getAskNum() {
		return askNum;
	}

	public void setAskNum(int askNum) {
		this.askNum = askNum;
	}

	public int getUserNum() {
		return userNum;
	}

	public void setUserNum(int userNum) {
		this.userNum = userNum;
	}

	public int getCategoryNum() {
		return categoryNum;
	}

	public void setCategoryNum(int categoryNum) {
		this.categoryNum = categoryNum;
	}

	public String getAskTitle() {
		return askTitle;
	}

	public void setAskTitle(String askTitle) {
		this.askTitle = askTitle;
	}

	public String getAskContent() {
		return askContent;
	}

	public void setAskContent(String askContent) {
		this.askContent = askContent;
	}

	public String getAskAttach() {
		return askAttach;
	}

	public void setAskAttach(String askAttach) {
		this.askAttach = askAttach;
	}

	public String getAskDate() {
		return askDate;
	}

	public void setAskDate(String askDate) {
		this.askDate = askDate;
	}

	public int getAskStatus() {
		return askStatus;
	}

	public void setAskStatus(int askStatus) {
		this.askStatus = askStatus;
	}

	public String getAskAnswer() {
		return askAnswer;
	}

	public void setAskAnswer(String askAnswer) {
		this.askAnswer = askAnswer;
	}


	
	

}
