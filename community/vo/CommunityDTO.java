package com.mommy.app.community.vo;

public class CommunityDTO {

	private int communityNum;
	private String communityTitle;
	private String communityContent;
	private int userNum;
	private String boardDate;
	private int communityCategory;
	private int userStatus;
	private String userId;
	private String fileName;
	
	public CommunityDTO() {;}

	public CommunityDTO(CommunityVO communityVO) {
		super();
		this.communityNum = communityVO.getCommunityNum();
		this.communityTitle = communityVO.getCommunityTitle();
		this.communityContent = communityVO.getCommunityContent();
		this.communityCategory = communityVO.getCommunityCategory();
		this.userNum = communityVO.getUserNum();
		this.boardDate = communityVO.getBoardDate();
		this.fileName = communityVO.getFileName();
	}
	
	public int getUserStatus() {
		return userStatus;
	}

	public void setUserStatus(int userStatus) {
		this.userStatus = userStatus;
	}
	
	public String getUserId() {
		return userId;
	}
	
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	public int getCommunityCategory() {
		return communityCategory;
	}

	public void setCommunityCategory(int communityCategory) {
		this.communityCategory = communityCategory;
	}
	
	
	public int getCommunityNum() {
		return communityNum;
	}

	public void setCommunityNum(int communityNum) {
		this.communityNum = communityNum;
	}
	
	public String getCommunityTitle() {
		return communityTitle;
	}

	public void setCommunityTitle(String communityTitle) {
		this.communityTitle = communityTitle;
	}

	public String getCommunityContent() {
		return communityContent;
	}

	public void setCommunityContent(String communityContent) {
		this.communityContent = communityContent;
	}

	public int getUserNum() {
		return userNum;
	}

	public void setUserNum(int userNum) {
		this.userNum = userNum;
	}

	public String getBoardDate() {
		return boardDate;
	}

	public void setBoardDate(String boardDate) {
		this.boardDate = boardDate;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	
	
	
}
